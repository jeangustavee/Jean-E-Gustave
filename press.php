            <h1>BIOGRAPHY</h1>
<p>Jean Edouard Gustave, was born January 15, 1986 in Jacmel, Haiti. Jean comes from a large family of six siblings, however, he only grew up with his two uterine siblings. While living in Haiti, he published his first book, Carrefour Maboulouloux, at the age of 18. The book was so popular that it completely sold out. Around the same time, he was able to drop his publisher and self-publish his second book,  La Cour Blasée. In 2007, and at the age of 21, Jean relocated to the United States to live with his father. As Jean left for the United States his mother, who is also his biggest inspiration, told him, “I believe in you and I’m certain you are going to make it there.” In 2009, after relocating, Jean published his third book, Lost Title. In 2011, he graduated with an Associates Degree in Electrical Technology. Soon after, he ran a magazine designed for the Haitian community called TopKreyol Magazine and was also the host of The TopKreyol Show for a year and a half.</p>

<p>By March of 2016, he graduated from Florida State University at Jacksonville and earned an Associates degree in Digital Multimedia Technology and Graphic Design Production. His next book, The 30th Glim, was completed and released just a month before his college graduation. In August of the same year, Jean with artist Victor Ali, held their first Art Exhibition called, The Tradigital Waves. After college, Jean turned down many job opportunities. He made these decisions because he knew exactly where he was meant to be, which was in Art and Business. Since childhood, Jean has had a passion for Art and Design. Determined to live his childhood dream, he stopped at nothing to make it work. Currently, Jean works as an Art Director and Partner with Antean Studios. Here, he oversees all operations and production of the company. His goal for himself, and his company Antean Studios, is to leave a mark in the Advertising and Marketing Industry. His ambition is to be an influence to many, and a well-known Artist, Art Director, Illustrator and Film Maker. Jean even has aspirations of lecturing at international conferences and teaching at universities.</p>

<p>Jean Edouard Gustave is an Artist, Entrepreneur, Art Director and Author, however, aside from these three, he likes to stay involved with his community. His Haitian heritage is very important to him and strives not only for himself, but also for those who find inspiration in him. His desire is to leave a legacy, not just for the Haitian community, but also for the Creative world. When he's not working on his legacy, Jean enjoys drawing and illustrating. He loves soccer, basketball and going to the movies. He is also talented in graphic design, coding, web design, interactive design, illustration, photography, film directing and painting.
</p>
            
